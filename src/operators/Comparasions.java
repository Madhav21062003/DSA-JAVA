package operators;

public class Comparasions {
    public static void main(String[] args) {

        // Comparison Operators are:  =, ==, >, <, >=, <=, !=
        int a = 7;
        int b = a;

        // Use of = operator
        System.out.println(b);

        // Use of == operator
        System.out.println(a==b);

        // Use of > operator
        System.out.println(a > b);

        // Use of < operator
        System.out.println(a < b);

        // Use of >= operator
        System.out.println(a >= b);

        // Use of <= operator
        System.out.println(a <= b);

        // Use of != operator
        System.out.println(a != b);
    }
}
