package operators;

public class Introduction {
    public static void main(String[] args) {

        // Arithmetic Operators
        // a) Unary b) Binary  --> +, -, *, /, %

        int num = 5;

        // Post Increment

        System.out.println(num++);
        System.out.println(num);


        // Pre Increment
        int n = 7;
        System.out.println(++n);
    }
}
