package operators;

public class Bitwise {
    public static void main(String[] args) {

        // Bitwise Operators are: &, |, ^, <<, >>

        // & operator
        int ans = 2 & 3;
        System.out.println(ans);

        ans = 2 | 3;
        System.out.println(ans);

        ans = 2 << 3;
        System.out.println(ans);

        ans = 2 >> 3;
        System.out.println(ans);

        ans = 2 ^ 3;
        System.out.println(ans);
    }
}
